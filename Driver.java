	public void driverMenu() {
		System.out.println(
			"\nSelect Driver run option:" +
			"\n1: Demonstration (run through each command with success/fail tests)" +
			"\n2: Benchmark (run through commands extensively)" +
			"\n3: Delete Database (seperated to prevent unwanted deletion)" +
			"\n4: Quit\n");
		System.out.print("Enter menu choice: ");
		String inputString = scan.nextLine();
		int choice = 0;
		try { choice = Integer.parseInt(inputString);
		} catch(NumberFormatException e) {choice = 0;}
		if(choice == 1)
			runDemonstration();
		else if(choice == 2)
			runBenchmark();
		else if(choice == 3)
			runDelete();
		else if(choice == 4) {
			System.out.println("EXITING");
			System.exit(0);
		}
		else {
			System.out.println("INVALID CHOICE");
			driverMenu();
		}
	}

